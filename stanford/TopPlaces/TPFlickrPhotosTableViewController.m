@synthesize photos = _photos;
- (void) setPhotos:(NSArray *)photos
{
    if(_photos != photos)
    {
        _photos = photos;
        if(self.tableView.window) [self.tableView reloadData];
    }
}
- (IBAction)refresh:(id)sender {
    UIActivityIndicatorView *spinner = [[UIActivityIndicatorView alloc]initWithActivityIndicatorStyle:UIActivityIndicatorViewStyleGray];
    [spinner startAnimating];
    self.navigationItem.rightBarButtonItem = [[UIBarButtonItem alloc] initWithCustomView:spinner];
    
    dispatch_queue_t downloadQueue = dispatch_queue_create("flickrDownloader",  NULL);
    dispatch_async(downloadQueue, ^{  // this is a block that gets executed in a separate thread
        NSArray *photos = [FlickrFetcher recentGeoreferencedPhotos];
        dispatch_async(dispatch_get_main_queue(), ^{ // within that separated thread a new block is created and queued in the main queue.
            self.navigationItem.rightBarButtonItem = sender;
            self.photos = photos;
        });
    });
    dispatch_release(downloadQueue);
}    


